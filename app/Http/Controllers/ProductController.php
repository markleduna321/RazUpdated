    /**
     * Store a newly created resource in storage.
     */
    public function store(StoreProductRequest $request)
    {
        $request->validate([
            'name' => 'required|string|max:255',
            'expiry_date' => 'required|date',
            'amount' => 'required|numeric',
        ]);

        // Create a new product
